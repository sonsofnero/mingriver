    <h1 class="h1">How To Enjoy</h1>
    <p>In China they say, “One cannot set the table without alcohol,” which means it’s not a meal without baijiu. Traditionally baijiu is served neat at room temperature to accompany food at mealtimes. It is poured into tiny, thimble-sized shot glasses that are downed in a series of communal toasts. One always drinks with others, so baijiu is associated with raucous, festive drinking sessions.</p>

    <p>China has no indigenous cocktail culture, but the Chinese are fond of infusing baijiu with fruits, spices, herbs and traditional Chinese medicine. These tonics are associated with health and wellness,but are usually not part of the toasting ritual. In recent years, bars in China and elsewhere have begun to experiment with baijiu in cocktails. As an ingredient, its strength and pungency make it challenging but rewarding to work with. It has tremendous untapped potential for adventuresome bartenders.</p>

