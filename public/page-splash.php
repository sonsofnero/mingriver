    <h1 class="h1">About Ming River</h1>
    <p>The journey to Ming River begins in the heart of Sichuan, China in the lush port city where China’s longest continuously operating distillery makes its legendary liquor. Over the past 700 years, traditional baijiu techniques here have passed from master to apprentice for over 20 generations.</p>

    <p>A balanced and complex spirit, Ming River is the original Sichuan baiju. Each batch starts with locally harvested red sorghum grain and the purest waters from protected wells. It is fermented in earthen pits with naturally harvested yeast cultures native to Luzhou that impart the distinctive terroir of Sichuan style baijiu. After two months, the mash is unearthed and distilled in small batches using a traditional Chinese pot still. The spirits age for up to two years before the master blender balances them into Ming River’s distinctive flavor.</p>

    <p>The result is an uncompromisingly bold spirit with notes of pineapple and anise with a lingering mellow finish. Perfect neat alongside spicy cuisine or in a cocktail, this baijiu evokes Sichuan with each sip.</p>

